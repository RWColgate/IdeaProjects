public class MyMath {
    public int countingAbsValueFromNumber(int a){
        return Math.abs(a);
    }
    public double countingAbsValueFromNumber(double a){
        return Math.abs(a);
    }
    public int countingPowerOfNumber(int a){
        return (int) Math.pow(a,2);
    }
    public double countingPowerOfNumber(double a){
        return Math.pow(a,2);
    }

}
